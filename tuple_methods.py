# 1. count() - Returns the number of times a specified value occurs in a tuple


print("1. count()\n")
tuple = ( 1, 2, 3, 4, 2, 2, 3)
x = tuple.count(2)
print(x)


# 2. index() - Searches the tuple for a specified value and returns the position of where it was found


print("\n\n")
print("2. index()\n")
tuple = ( 1, 2, 3, 4, 2, 2, 3)
x = tuple.index(2)
print(x)
